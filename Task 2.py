xc = int(input('Введите координату x центра окружности\n'))

yc = int(input('Введите координату y центра окружности\n'))

r = int(input('Введите радиус окружности\n'))

x = int(input('Введите координату x точки\n'))

y = int(input('Введите координату y точки\n'))

if (abs((x-xc))**2 + abs((y-yc))**2)**(1/2) < r:
    print('Точка внутри окружности')
else:
    print('Точка вне окружности')